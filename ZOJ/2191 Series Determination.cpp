/*
对于一个二次函数，给出f(0),f(1),f(2)的值，求f(3),f(4),f(5)的值

可以先根据f(0),f(1),f(2)算出二次函数三项的系数，确定方程以后求f(3),f(4),f(5)。
或可以推得：f(3)=3[f(2)-f(1)]+f(0)
            f(4)=2[3f(2)-4f(1)]+3f(0)
            f(5)=5[2f(2)-3f(1)]+6f(0)直接求解

关键词：数学题，巧算法
*/
#include<iostream>
using namespace std;

int main()
{
    int f0,f1,f2;
    
    while(cin>>f0>>f1>>f2)
        cout<<3*(f2-f1)+f0<<" "<<2*(3*f2-4*f1)+3*f0<<" "<<5*(2*f2-3*f1)+6*f0<<endl;
    
    return 0;
}
