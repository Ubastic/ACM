#pragma argsused
#include <iostream.h>
int main(int argc, char* argv[])
{
  int n,m,w,i,j,s,k,r,a[10010],b[10010],c[10010];
  a[0]=0; b[0]=0;
  while (cin>>n>>m)
  {
    for (i=1;i<=n;i++)
    {
      cin>>w;
      a[i]=(a[i-1]+w)%m;
      c[i]=0;
    }
    w=0; s=1; c[0]=0;
    for (i=1;i<=n;i++)
    {
      for (j=0;j<s;j++)
        if (a[i]==b[j])
        { w+=++c[j]; break;}
      if (s==j)
        b[s++]=a[i];
    }
    cout<<w<<endl;
  }
  return 0;
}
